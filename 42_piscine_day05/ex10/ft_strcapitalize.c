char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] <= 'z')
			|| (str[i] >= 'A' && str[i] <= 'Z'))
		{
			if (!(str[i - 1] >= 'a' && str[i - 1] <= 'z')
				&& !(str[i - 1] >= 'A' && str[i - 1] <= 'Z'))
			{
				if (str[i] >= 'a' && str[i] <= 'z')
					str[i] -= 32;
				else if (str[i] >= 'A' && str[i] <= 'Z')
					str[i] += 32;
			}
		}
		i++;
	}
	return (str);
}

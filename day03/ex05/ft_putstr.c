#include "ft_putchar.h"

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (1)
	{
		if (str[i] == '\0')
		{
			return ;
		}
		else
		{
			ft_putchar(str[i]);
			i++;
		}
	}
}
